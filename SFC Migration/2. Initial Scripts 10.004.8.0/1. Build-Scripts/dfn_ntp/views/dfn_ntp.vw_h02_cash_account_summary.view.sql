CREATE OR REPLACE FORCE VIEW dfn_ntp.vw_h02_cash_account_summary
(
    h02_cash_account_id_u06,
    h02_date,
    h02_customer_id_u01,
    h02_currency_code_m03,
    h02_balance,
    h02_blocked,
    h02_open_buy_blocked,
    h02_payable_blocked,
    h02_manual_trade_blocked,
    h02_manual_full_blocked,
    h02_manual_transfer_blocked,
    h02_receivable_amount,
    h02_currency_id_m03,
    h02_margin_enabled,
    h02_pending_deposit,
    h02_pending_withdraw,
    h02_primary_od_limit,
    h02_primary_start,
    h02_primary_expiry,
    h02_secondary_od_limit,
    h02_secondary_start,
    h02_secondary_expiry,
    h02_investment_account_no,
    h02_daily_withdraw_limit,
    h02_daily_cum_withdraw_amt,
    h02_margin_due,
    h02_margin_block,
    h02_margin_product_id_u23,
    h02_net_receivable,
    h02_opening_balance,
    h02_deposits,
    h02_withdrawals,
    h02_net_buy,
    h02_net_sell,
    h02_net_charges_refunds,
    h02_net_commission,
    h02_margin_utilized,
    h02_gainloss,
    h02_loan_amount
)
AS
    SELECT h02.h02_cash_account_id_u06,
           h00.h00_date AS h02_date,
           h02.h02_customer_id_u01,
           h02.h02_currency_code_m03,
           h02.h02_balance,
           h02.h02_blocked,
           h02.h02_open_buy_blocked,
           h02.h02_payable_blocked,
           h02.h02_manual_trade_blocked,
           h02.h02_manual_full_blocked,
           h02.h02_manual_transfer_blocked,
           h02.h02_receivable_amount,
           h02.h02_currency_id_m03,
           h02.h02_margin_enabled,
           h02.h02_pending_deposit,
           h02.h02_pending_withdraw,
           h02.h02_primary_od_limit,
           h02.h02_primary_start,
           h02.h02_primary_expiry,
           h02.h02_secondary_od_limit,
           h02.h02_secondary_start,
           h02.h02_secondary_expiry,
           h02.h02_investment_account_no,
           h02.h02_daily_withdraw_limit,
           h02.h02_daily_cum_withdraw_amt,
           h02.h02_margin_due,
           h02.h02_margin_block,
           h02.h02_margin_product_id_u23,
           h02.h02_net_receivable,
           h02.h02_opening_balance,
           h02.h02_deposits,
           h02.h02_withdrawals,
           h02.h02_net_buy,
           h02.h02_net_sell,
           h02.h02_net_charges_refunds,
           h02.h02_net_commission,
           h02.h02_margin_utilized,
           h02.h02_gainloss,
           h02_loan_amount
      FROM h02_cash_account_summary_all h02
           JOIN u06_cash_account u06
               ON h02.h02_cash_account_id_u06 = u06.u06_id
           JOIN h00_dates h00
               ON h02.h02_date =
                      fn_get_latest_h02_date (u06.u06_id, h00.h00_date)
/
