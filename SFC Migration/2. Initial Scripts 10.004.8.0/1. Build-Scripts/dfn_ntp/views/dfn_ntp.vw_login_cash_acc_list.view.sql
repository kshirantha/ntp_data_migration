           u01.u01_display_name,
           u01.u01_display_name_lang,
           u17_status_changed_by.u17_full_name AS status_changed_by_name
      FROM u30_login_cash_acc u30
           LEFT JOIN vw_status_list status_list
               ON u30.u30_status_id_v01 = status_list.v01_id
           LEFT JOIN u17_employee u17_status_changed_by
               ON u30.u30_status_changed_by_id_u17 =
                      u17_status_changed_by.u17_id
           INNER JOIN u06_cash_account u06
               ON u30.u30_cash_acc_id_u06 = u06.u06_id
           INNER JOIN u01_customer u01
               ON u06.u06_customer_id_u01 = u01.u01_id
