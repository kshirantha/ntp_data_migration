AS
    SELECT u30.u30_id,
           u30.u30_login_id_u09,
           u30.u30_cash_acc_id_u06,
           u30.u30_created_by_id_u17,
           u30.u30_created_date,
           u30.u30_deposit,
           u30.u30_withdraw,
           u30.u30_transfer,
           u30.u30_pending_restriction,
           u30.u30_status_id_v01,
           u30.u30_status_changed_by_id_u17,
           u30.u30_status_changed_date,
           status_list.v01_description AS status_description,
           status_list.v01_description_lang AS status_description_lang,
           u06.u06_id,
           u06.u06_institute_id_m02,
           u06.u06_customer_id_u01,
           u06.u06_customer_no_u01,
           u06.u06_display_name_u01,
           u06.u06_default_id_no_u01,
