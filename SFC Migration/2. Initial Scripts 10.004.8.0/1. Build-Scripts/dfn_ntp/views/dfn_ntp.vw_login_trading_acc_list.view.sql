AS
    SELECT u10.u10_id,
           u10.u10_login_id_u09,
           u10.u10_trading_acc_id_u07,
           u10.u10_created_by_id_u17,
           u10.u10_created_date,
           u10.u10_buy,
           u10.u10_sell,
           u10.u10_deposit,
           u10.u10_withdraw,
           u10.u10_transfer,
           u10.u10_pending_restriction,
           u10.u10_status_id_v01,
           u10.u10_status_changed_by_id_u17,
           u10.u10_status_changed_date,
           status_list.v01_description AS status_description,
           status_list.v01_description_lang AS status_description_lang,
           u07.u07_id,
           u07.u07_institute_id_m02,
           u07.u07_customer_id_u01,
           u07.u07_cash_account_id_u06,
           u07.u07_exchange_code_m01,
