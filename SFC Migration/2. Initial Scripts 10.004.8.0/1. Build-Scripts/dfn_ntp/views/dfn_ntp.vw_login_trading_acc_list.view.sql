    u10_id,
    u10_login_id_u09,
    u10_trading_acc_id_u07,
    u10_created_by_id_u17,
    u10_created_date,
    u10_buy,
    u10_sell,
    u10_deposit,
    u10_withdraw,
    u10_transfer,
    u10_pending_restriction,
    u10_status_id_v01,
    u10_status_changed_by_id_u17,
    u10_status_changed_date,
    status_description,
    status_description_lang,
    u07_id,
    u07_institute_id_m02,
    u07_customer_id_u01,
    u07_cash_account_id_u06,
    u07_exchange_code_m01,
    u07_display_name_u06,
    u07_customer_no_u01,
    u07_display_name_u01,
    u07_default_id_no_u01,
    u07_is_default,
    u07_type,
    u07_trading_enabled,
    u07_sharia_compliant,
    u07_trading_group_id_m08,
    u07_created_by_id_u17,
    u07_created_date,
    u07_status_id_v01,
    u07_commission_group_id_m22,
    u07_discount_percentage,
    u07_commission_dis_grp_id_m24,
    u07_modified_by_id_u17,
    u07_modified_date,
    u07_status_changed_by_id_u17,
    u07_status_changed_date,
    u07_exe_broker_id_m26,
    u07_display_name,
