           u07.u07_txn_fee,
           u07.u07_cust_settle_group_id_m35,
           u07.u07_custodian_id_m26,
           u17_status_changed_by.u17_full_name AS status_changed_by_name
      FROM u10_login_trading_acc u10
           LEFT JOIN vw_status_list status_list
               ON u10.u10_status_id_v01 = status_list.v01_id
           LEFT JOIN u17_employee u17_status_changed_by
               ON u10.u10_status_changed_by_id_u17 =
                      u17_status_changed_by.u17_id
           INNER JOIN u07_trading_account u07
               ON u10.u10_trading_acc_id_u07 = u07.u07_id
