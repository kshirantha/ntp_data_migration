(
    t38_cond_order_id,
    t38_trading_acc_id_u07,
    t38_customer_id_u01,
    t38_cash_acc_id_u06,
    t38_symbol_id_m20,
    t38_condition,
    t38_condition_status,
    t38_expiry_date,
    t38_created_date,
    t38_triggered_date,
    created_by,
    t38_reject_reason,
    t38_instrument_type_code,
    t38_strategy_name,
    t38_orig_cl_ord_id,
    t38_symbol_code_m20,
    t38_quantity,
    t38_price,
    u07_trading_group_id_m08,
    t38_side,
    t38_ord_type_id_v06,
    t38_tif_id_v10,
    t38_exchange_code_m01,
    t38_cum_quantity,
    t38_ord_channel_id_v29,
    t38_desk_order_ref_t52,
    t38_institution_id_m02,
    t38_exec_type,
    t38_trig_interval,
    t38_block_size,
    t38_cum_net_value,
    t38_cum_netstl,
    t38_trig_ord_count,
    t38_ord_no,
    t38_ord_category,
    t38_last_updated_date_time,
    t38_customer_no,
    t38_currency,
    t38_stop_px,
    u01_display_name,
    u01_full_name,
    u01_external_ref_no,
    u07_display_name,
    t38_triggered_time,
    t38_triggered_price,
    m01_offline_feed,
    t38_cond_exp_time
)
AS
    SELECT t38.t38_cond_order_id,
           t38.t38_trading_acc_id_u07,
           t38.t38_customer_id_u01,
           t38.t38_cash_acc_id_u06,
           t38.t38_symbol_id_m20,
           t38.t38_condition,
           t38.t38_condition_status,
           t38.t38_expiry_date,
           t38.t38_created_date,
           t38.t38_triggered_date,
           u17.u17_login_name AS created_by,
           t38.t38_reject_reason,
           t38.t38_instrument_type_code,
           t38.t38_strategy_name,
           t38.t38_orig_cl_ord_id,
           t38.t38_symbol_code_m20,
           t38.t38_quantity,
           t38.t38_price,
           u07.u07_trading_group_id_m08,
           t38.t38_side,
           t38.t38_ord_type_id_v06,
           t38.t38_tif_id_v10,
           t38.t38_exchange_code_m01,
           t38.t38_cum_quantity,
           t38.t38_ord_channel_id_v29,
           t38.t38_desk_order_ref_t52,
           t38.t38_institution_id_m02,
           t38.t38_exec_type,
           t38.t38_trig_interval,
           t38.t38_block_size,
           t38.t38_cum_net_value,
           t38.t38_cum_netstl,
           t38.t38_trig_ord_count,
           t38.t38_ord_no,
           t38.t38_ord_category,
           t38.t38_last_updated_date_time,
           t38.t38_customer_no,
           t38.t38_currency,
           t38.t38_stop_px,
           u01.u01_display_name,
           u01.u01_full_name,
           u01.u01_external_ref_no,
           u07.u07_display_name,
           t38.t38_triggered_time,
           t38.t38_triggered_price,
           m01.m01_offline_feed,
           t38.t38_cond_exp_time
      FROM t38_conditional_order t38
           JOIN u07_trading_account u07
               ON t38.t38_trading_acc_id_u07 = u07.u07_id
           JOIN u01_customer u01
               ON t38.t38_customer_id_u01 = u01.u01_id
           LEFT JOIN u17_employee u17
               ON t38.t38_dealer_id_u17 = u17.u17_id
           JOIN m01_exchanges m01
               ON     t38.t38_institution_id_m02 = m01.m01_institute_id_m02
                  AND t38.t38_exchange_code_m01 = m01.m01_exchange_code
/