CREATE OR REPLACE FORCE VIEW dfn_ntp.vw_symbol_price_upload_detail
(
    m160_id,
    m160_symbol,
    m160_date,
    m160_status,
    status_description,
    m160_old_ltp,
    m160_new_ltp,
    m160_old_previous_close,
    m160_new_previous_close,
    m160_old_last_trade_date,
    m160_new_last_trade_date,
    m160_old_max_price,
    m160_new_max_price,
    m160_old_bid_price,
    m160_new_bid_price,
    m160_old_ask_price,
    m160_new_ask_price,
    m160_old_maturity_date,
    m160_new_maturity_date,
    m160_old_min_price,
    m160_new_min_price,
    m160_updated_by_id_u17,
    m160_updated_date,
    m160_reason,
    m160_reuters_code,
    m160_currency,
    m160_exchange,
    m160_volume,
    m160_coupon_rate,
    m160_isin,
    m160_custom_type,
    m160_symbol_sessions_id_m159
)
AS
    SELECT m160_id,
           m160_symbol,
           m160_date,
           m160_status,
           DECODE (m160_status,  3, 'Rejected',  17, 'Processed')
               AS status_description,
           m160_old_ltp,
           m160_new_ltp,
           m160_old_previous_close,
           m160_new_previous_close,
           m160_old_last_trade_date,
           m160_new_last_trade_date,
           m160_old_max_price,
           m160_new_max_price,
           m160_old_bid_price,
           m160_new_bid_price,
           m160_old_ask_price,
           m160_new_ask_price,
           m160_old_maturity_date,
           m160_new_maturity_date,
           m160_old_min_price,
           m160_new_min_price,
           m160_updated_by_id_u17,
           m160_updated_date,
           m160_reason,
           m160_reuters_code,
           m160_currency,
           m160_exchange,
           m160_volume,
           m160_coupon_rate,
           m160_isin,
           m160_custom_type,
           m160_symbol_sessions_id_m159
      FROM m160_offline_symbol_update_log
/