declare
  l_count number;
begin
  select count(*) into l_count from user_objects where object_name = 'E_TMP' ;

  if l_count = 1
  then
     execute immediate 'drop table e_tmp';
  end if;
end;
/

  CREATE TABLE "E_TMP" 
   (	"Z08_SQL" VARCHAR2(200) , 
	"Z08_VERSION" NUMBER(18,0) , 
	"Z08_ENABLED" NUMBER(1,0) , 
	"Z01_TYPE" NUMBER(2,0) , 
	"Z01_ID" NUMBER(5,0), 
	"Z08_LAST_UPDATED_DATETIME" DATE
   ) 
   ORGANIZATION EXTERNAL 
    ( TYPE ORACLE_DATAPUMP
      DEFAULT DIRECTORY "DUMPDIR"
      ACCESS PARAMETERS
      ( version '11.2'    )
      LOCATION
       ( 'dfn_ntp.z08_version.dat'
       )
    )
/


begin
  insert into dfn_ntp.z08_version select * from e_tmp;

  commit;
end;
